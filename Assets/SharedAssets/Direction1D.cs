/// <summary>
/// Represents a direction in one dimension
/// </summary>
public enum Direction1D
{
    /// <summary>
    /// No direction. Often invalid
    /// </summary>
    NONE = 0,

    /// <summary>
    /// Descending, left or down
    /// </summary>
    LEFT = -1,

    /// <summary>
    /// Ascending, right or up
    /// </summary>
    RIGHT = 1
}